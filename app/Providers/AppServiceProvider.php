namespace Traq\Providers;

use Illuminate\Support\ServiceProvider;

class AppServiceProvider extends ServiceProvider
{
    /**
     * Register any application services.
     *
     * @return void
     */
    public function register()
    {
        if ($this->app->isLocal()) {
            $this->app->register(\Traq\Providers\TelescopeServiceProvider::class);
            $this->app->register(\Barryvdh\LaravelIdeHelper\IdeHelperServiceProvider::class);
        }
    }

    /**
     * @throws \Illuminate\Contracts\Container\BindingResolutionException
     */
    public function boot()
    {
        $versionFilePath = $this->app->resourcePath('version.php');

        if (file_exists($versionFilePath)) {
            $versionInfo = require $versionFilePath;
            $config = $this->app->make('config');

            $config->set([
                'traq.version' => $versionInfo['version'],
            ]);
        }
    }
}
